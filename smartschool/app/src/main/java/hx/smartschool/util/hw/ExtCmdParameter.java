package hx.smartschool.util.hw;


public class ExtCmdParameter {

    private int Steps;
    private int Tumblings;
    private int BatteryPercentage;
    private String HwVersion;
    private int HwHeartBeat;
    private int HwStatus;
    private String SatelliteNum;
    private String LocationStatus;
    private String Latitude;
    private String Longitude;


    public int getSteps() {
        return Steps;
    }

    public void setSteps(int steps) {
        Steps = steps;
    }

    public int getTumblings() {
        return Tumblings;
    }

    public void setTumblings(int tumblings) {
        Tumblings = tumblings;
    }

    public int getBatteryPercentage() {
        return BatteryPercentage;
    }

    public void setBatteryPercentage(int batteryPercentage) {
        BatteryPercentage = batteryPercentage;
    }

    public String getHwVersion() {
        return HwVersion;
    }

    public void setHwVersion(String hwVersion) {
        HwVersion = hwVersion;
    }

    public int getHwHeartBeat() {
        return HwHeartBeat;
    }

    public void setHwHeartBeat(int hwHeartBeat) {
        HwHeartBeat = hwHeartBeat;
    }

    public int getHwStatus() {
        return HwStatus;
    }

    public void setHwStatus(int hwStatus) {
        HwStatus = hwStatus;
    }

    public String getSatelliteNum() {
        return SatelliteNum;
    }

    public void setSatelliteNum(String satelliteNum) {
        SatelliteNum = satelliteNum;
    }

    public String getLocationStatus() {
        return LocationStatus;
    }

    public void setLocationStatus(String locationStatus) {
        LocationStatus = locationStatus;
    }

    public String getLatitude() {
        return Latitude;
    }

    public void setLatitude(String latitude) {
        Latitude = latitude;
    }

    public String getLongitude() {
        return Longitude;
    }

    public void setLongitude(String longitude) {
        Longitude = longitude;
    }
}